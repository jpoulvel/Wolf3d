#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 1)
		return (1);
	while (i < nb)
	{
		if (((nb / i) == i) && (nb % i == 0))
			return (i);
		else
			i++;
	}
	return (0);
}
